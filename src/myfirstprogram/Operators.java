package myfirstprogram;

public class Operators {

	public static void main(String[] args) {
	//Addition
		int i=10;
		int j=25;
		int k=i+j;
		System.out.println(k);
	//Subtraction
		int a=23;
		int b=5;
		int c=a-b;
		System.out.println(c);
	//Multiplication
		int m=10;
		int n=4;
		int o=m*n;
		System.out.println(o);
	// Division
		int e=7;
		int f=2;
		int g=e/f;
		System.out.println(g);
	//decimals
		float u=10.3f;
		float v=11f;
		float x=u/v;
		System.out.println(x);
  
	}

}
